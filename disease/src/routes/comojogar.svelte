<p>
    Neste jogo cada jogador recebe um deck específico de 44 cartas, onde as
    cartas do invasor representam as doenças e situações adversas, e as cartas
    do defensor representam os tratamentos e remediações para as situações e
    doenças que aparecem ao longo do jogo.<br />
    Na primeira rodada o jogador saca 5 cartas do deck, a cada rodada 1 carta deve
    ser puxada do deck e pelo menos 1 carta deve ser colocada na mesa.<br />
    As cartas colocadas na mesa devem ser viradas para baixo.<br />
    No final da rodada cada jogador deve revelar pelo menos uma carta que está virada
    para baixo na mesa.<br />
    O invasor deve revelar primeiro. Para cada jogador no máximo 5 cartas podem estar
    na mesa simultaneamente.<br />
    Quando uma carta vai para a mesa seu efeito passa a valer (mesmo quando ela estiver
    virada para baixo).<br />
    Uma carta de doença virada para baixo passa a incrementar seu dano a cada rodada
    que permanecer na mesa, mas seu dano somente terá efeito no defensor a partir
    da rodada que ela for revelada.<br />
    Uma carta de defesa será ativada quando ela for revelada.<br />
    Uma vez ativada, uma carta de defesa deixa a mesa e vai para o cemitério.<br
    />
    Uma carta de ataque deixará a mesa e seguirá para o cemitério caso uma carta
    de defesa compatível seja ativada.<br />
    O total de pontos de vida do defensor é 100.<br />
    Caso o invasor fique sem cartas em seu deck o defensor vence o jogo.<br />
    Caso o defensor fique sem pontos de vida antes do fim do deck do invasor, o invasor
    vence o jogo.<br />
    As cartas do invasor são as pretas.<br />
    As cartas do defensor são as azuis.<br />
</p>
